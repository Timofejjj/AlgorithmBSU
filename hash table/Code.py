# Читаем m, c, n из первой строки
m, c, n = map(int, input().split())

# Инициализируем хеш-таблицу размером m, заполненную -1 (пустые ячейки)
table = [-1] * m

# Обрабатываем каждый ключ
for _ in range(n):
    x = int(input())  # Читаем следующий ключ
    
    i = 0  # Начинаем с первой попытки
    while True:
        # Вычисляем индекс с учетом линейного пробирования
        index = ((x % m) + (c * i)) % m
        
        if table[index] == -1:
            # Если ячейка пуста, вставляем ключ и завершаем
            table[index] = x
            break
        elif table[index] == x:
            # Если ключ уже есть, ничего не делаем и завершаем
            break
        else:
            # Если ячейка занята другим ключом, продолжаем пробирование
            i += 1

# Выводим итоговое состояние таблицы, соединяя элементы пробелами
print(' '.join(map(str, table)))